		/// <summary>
		/// Generates markup to close the last <paramref name="count"/> opened <c>Component</c>,
		/// <c>Element</c> or <c>Region</c> blocks.
		/// </summary>
		/// <remarks>
		/// <param name="count">The number of blocks to close.</param>
		/// <param name="prettyPrint"><c>true</c> to insert newline and indent whitespace before
		/// the markup for this element, as long as <see cref="PrettyPrinting"/> is enabled.</param>
		/// <param name="line">The source code line number used to generate the sequence number.</param>
		/// Calls to this method must match calls to <see cref="OpenComponent(Type, bool, int)">OpenComponent</see>,
		/// <see cref="OpenElement(string, bool, int)">OpenElement</see> and
		/// <see cref="OpenRegion(int)">OpenRegion</see>.
		/// </remarks>
		public FluentRenderTreeBuilder Close(int count, bool prettyPrint = true, [CallerLineNumber] int line = 0)
		{
			if (count < 1)
				throw new ArgumentException("Count must be positive", nameof(count));

			while (count-- > 0)
				DoClose(prettyPrint, line);

			return this;
		}

