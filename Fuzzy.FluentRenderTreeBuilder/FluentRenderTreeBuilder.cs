		/// <summary>
		/// Outputs newline(s) and tab(s) to match the current indent level, if
		/// <see cref="PrettyPrinting"/> is enabled.
		/// </summary>
		/// <param name="line">The optional source code line, or -1 to use <see cref="_currentLine"/>.</param>
		/// <param name="offset">The optional offset to add to the current indent.</param>
		/// <param name="newlineCount">The number of newlines to emit, defaulting to 1.</param>
		/// <param name="suppressPrettyPrint">
		/// <c>true</c> to prevent any newlines or tabs being emitted, even if
		/// <see cref="PrettyPrinting"/> is enabled (unless <paramref name="forceNewlines"/> is
		/// <c>true</c>); or <c>false</c> to perform default pretty-printing behaviour, which is
		/// to emit newline(s) and tab(s) if <see cref="PrettyPrinting"/> is enabled.
		/// </param>
		/// <param name="forceNewlines"></param>
		FluentRenderTreeBuilder PrettyPrint(int line = -1, int offset = 0,
				int newlineCount = 1, bool suppressPrettyPrint = false, bool forceNewlines = false)
		{
			var doIndent = _prettyPrint && !suppressPrettyPrint;
			var doNewlines = doIndent || forceNewlines;

			if (!doIndent && !doNewlines)
				return this; // nothing to do

			if (line == -1)
				line = _currentLine;

			// STILL no `new string ("string", n)` in C#/.NET, so do it with string.Replace() :(
			var newlines = !doNewlines ? "" : newlineCount == 1 ? Environment.NewLine :
				newlineCount > 1 ? new string('X', newlineCount).Replace("X", Environment.NewLine) : "";

			var indent = !doIndent ? "" : new string('\t', _initialIndent + CurrentIndent + offset);

			_builder.AddMarkupContent(GetSequence(line), newlines + indent);

			return this;
		}

		/// <summary>
		/// Gets the sequence number for the given source code line.
		/// </summary>
		/// <remarks>
		/// See the <see cref="FluentRenderTreeBuilder"/> overview for details on sequence number
		/// generation and an example showing how it works.
		/// </remarks>
		/// <param name="line">The source code line from which to generate the sequence number.</param>
		/// <param name="callerName">The name of the calling member.</param>
		int GetSequence(int line, [CallerMemberName] string callerName = "")
		{
			if (line == _currentLine)
			{
				++_sequence;

				if (_sequence == (_currentLine + 1) * _maxPerLine)
					throw new InvalidOperationException(
							$"Only {_maxPerLine} operations allowed on one source code line");
			}
			else
			{
				_currentLine = line;
				_sequence = line * _maxPerLine;
			}

			if (_logger?.IsEnabled(LogLevel.Information) ?? false)
			{
				FrameType frameType = FrameType.None;
				string? name = null;

				if (_frames.Count > 0)
					(frameType, name) = _frames.Peek();

				var type = frameType == FrameType.None ? "top level" : $"{frameType}";
				var info = callerName switch
				{
					var n when n == nameof(OpenComponent) ||
							   n == nameof(OpenRegion) ||
							   n == nameof(Content) ||
							   n == nameof(Markup) ||
							   n == nameof(PrettyPrint) => $"{callerName} in {type}",
					nameof(Attribute) => $"{callerName} on {type}",
					_ => callerName
				};

				if (info == "" && !string.IsNullOrEmpty(name))
					info = "in";

				_logger.LogInformation($"GetSequence: Sequence number {_sequence} " +
						$"generated for {info} {name}");
			}

			return _sequence;
		}

