		/// Generates markup to close last opened <c>Component</c>, <c>Element</c> or
		/// <c>Region</c> block.
		/// </summary>
		/// <remarks>
		/// <param name="prettyPrint"><c>true</c> to insert newline and indent whitespace before
		/// the closing markup for this element, unless closing a region in which case no output is
		/// the markup for this element, as long as <see cref="PrettyPrinting"/> is enabled.</param>
		/// <param name="line">The source code line number used to generate the sequence number.</param>
		/// Calls to this method must match calls to
		/// <see cref="OpenComponent(Type, bool, int)">OpenComponent</see>,
		/// <see cref="OpenElement(string, bool, int)">OpenElement</see> and
		/// <see cref="OpenRegion(int)">OpenRegion</see>.
		/// </remarks>
		public FluentRenderTreeBuilder Close(bool prettyPrint = true, [CallerLineNumber] int line = 0)
			=> DoClose(prettyPrint, line);

		/// <summary>
		/// Registers a helper callback to be invoked when <see cref="Close(bool, int)">Close</see>
		/// is called for the last opened <c>Component</c>, <c>Element</c> or <c>Region</c> block.
		/// </summary>
		/// <param name="helper">The helper callback to invoke.</param>
		public FluentRenderTreeBuilder CloseHelper(Action<int> helper)
		{
			if (helper == null)
				throw new ArgumentNullException("A helper callback must be specified", nameof(helper));

			_frames.Push(new FrameInfo(helper));
			++_closeHelpers;

			return this;
		}

		FluentRenderTreeBuilder DoClose(bool prettyPrint, int line)
		{
			// pop this first so PrettyPrint below produces the correct indent
			var (frameType, name, closeHelper) = _frames.Pop();

			_logger?.LogInformation($"DoClose: Closing {frameType} {name}");

			if (prettyPrint && frameType != FrameType.Region && frameType != FrameType.CloseHelper)
				PrettyPrint(line);

			switch (frameType)
			{
				case FrameType.Component: _builder.CloseComponent(); break;
				case FrameType.Element: _builder.CloseElement(); break;
				case FrameType.Region: _builder.CloseRegion(); break;

				case FrameType.CloseHelper:
					--_closeHelpers;

					closeHelper.Invoke(line);
					break;
			};

			return this;
		}

